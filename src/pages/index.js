export async function getStaticProps() {
  const filePath = path.join(process.cwd(), 'src/data/homepage.json');
  const jsonData = fs.readFileSync(filePath, 'utf8');
  const data = JSON.parse(jsonData) || {};

  return {
    props: {
      ...data
    },
  };
}